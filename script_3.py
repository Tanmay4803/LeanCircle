# Create the database configuration (src/config/database.js)
database_config_content = '''// src/config/database.js - MongoDB connection configuration
const mongoose = require('mongoose');

const connectDB = async () => {
  try {
    const mongoURI = process.env.MONGODB_URI || 'mongodb://127.0.0.1:27017/leancircle-hr';
    
    const options = {
      useNewUrlParser: true,
      useUnifiedTopology: true,
      maxPoolSize: 10, // Maintain up to 10 socket connections
      serverSelectionTimeoutMS: 5000, // Keep trying to send operations for 5 seconds
      socketTimeoutMS: 45000, // Close sockets after 45 seconds of inactivity
      family: 4 // Use IPv4, skip trying IPv6
    };

    const conn = await mongoose.connect(mongoURI, options);

    console.log(`‚úÖ MongoDB Connected: ${conn.connection.host}`);
    console.log(`üìç Database: ${conn.connection.name}`);

    // Handle connection events
    mongoose.connection.on('error', (err) => {
      console.error('‚ùå MongoDB connection error:', err);
    });

    mongoose.connection.on('disconnected', () => {
      console.warn('‚ö†Ô∏è  MongoDB disconnected');
    });

    mongoose.connection.on('reconnected', () => {
      console.log('üîÑ MongoDB reconnected');
    });

    // Graceful exit
    process.on('SIGINT', async () => {
      await mongoose.connection.close();
      console.log('üîå MongoDB connection closed through app termination');
      process.exit(0);
    });

  } catch (error) {
    console.error('‚ùå MongoDB connection failed:', error.message);
    process.exit(1);
  }
};

module.exports = connectDB;
'''

# Create config directory and write database.js
os.makedirs('src/config', exist_ok=True)

with open('src/config/database.js', 'w') as f:
    f.write(database_config_content)

print("‚úÖ Created src/config/database.js")